     public void Load_Date() {
         Connection con=ConnectionProvider.getCon();
    try {
        pst = con.prepareStatement("select Distinct datee from date");
        rs = pst.executeQuery();
        CboDate.removeAllItems();

        while (rs.next()) {
            CboDate.addItem(rs.getString("datee"));
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}

