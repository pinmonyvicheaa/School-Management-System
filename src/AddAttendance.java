    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          Connection con=ConnectionProvider.getCon();
        try {
            String date1 = (String)CboDate.getSelectedItem();
            String studentname = (String)CboStuName.getSelectedItem();
            String subject = (String)CboSubject.getSelectedItem();
            String status1 = (String)cboStatus.getSelectedItem();
            String days = (String)Cbodays.getSelectedItem();
            String time = txtTime.getText();

            pst = con.prepareStatement("INSERT INTO attendance(date,stuname,subjects,status,days,time) VALUES (?,?,?,?,?,?)");
            pst.setString(1, date1);
            pst.setString(2, studentname);
            pst.setString(3, subject);
            pst.setString(4, status1);
            pst.setString(5, days);
            pst.setString(6, time);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Timetable Registration Successful");

            CboDate.setSelectedIndex(-1);
            CboStuName.setSelectedIndex(-1);
            CboSubject.setSelectedIndex(-1);
            cboStatus.setSelectedIndex(-1);
            Cbodays.setSelectedIndex(-1);
            txtTime.setText("");

        } catch (SQLException ex) {
            Logger.getLogger(TimeReg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Connection con=ConnectionProvider.getCon();
        try {
        String stunamee = txtStuname.getText();

        pst = con.prepareStatement("DELETE FROM stuname WHERE stunamee=?");
        pst.setString(1, stunamee);

        int deletedRows = pst.executeUpdate();
        if (deletedRows > 0) {
            JOptionPane.showMessageDialog(this, "Record Deleted Successfully");
            Load_Stuname(); // Reload class list after deletion
        } else {
            JOptionPane.showMessageDialog(this, "No record found for the given Student Name.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException ex) {
        Logger.getLogger(AddAttendance.class.getName()).log(Level.SEVERE, null, ex);
    }
          txtStuname.setText("");
    }//GEN-LAST:event_jButton5ActionPerformed

